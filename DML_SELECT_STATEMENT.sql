-- EMPLOYEES TABLOSUNDAKÝ MANGER ID KOLONUNU EKRANA YAZDIRIRKEN KOLON ADINI "M", FIRST NAME KOLONUNU ÝSE "ADI" OLARAK YAZDIRSIN.
