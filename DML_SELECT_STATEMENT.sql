-- 90 NOLU DEPARTMANDA BULUNANLARI LÝSTELESÝN.
SELECT * FROM EMPLOYEES WHERE DEPARTMENT_ID = 90;

-- 90 VEYA 100 NOLU DEPARTMANDA BULUNANLARI LÝSTELESÝN.
SELECT * FROM EMPLOYEES WHERE DEPARMENT_ID IN (90,100);
SELECT * FROM EMPLOYEES WHERE DEPARTMENT_IS = 90 OR DEPARTMENT_ID = 100;

-- LAST NAME'ÝN BAÞINDA M HARFÝ BULUNANLARI LÝSTELESÝN.
SELECT * FROM EMPLOYEES WHERE LAST_NAME LIKE 'M%';

-- LAST NAME'IN ORTASINDA M HARFÝ GEÇENLERÝ LÝSTELESÝN.
SELECT * FROM EMPLOYEES WHERE LAST_NAME LIKE '%M%';

-- LAST NAME'ÝN ÝKÝNCÝ KARAKTERÝ A VE DÖRDÜNCÜ KARAKTERÝ E OLANLARI LÝSTELESÝN.
SELECT * FROM EMPLOYEES WHERE LAST_NAME LIKE '_A_E%';

-- MAAÞI 1000'DEN BÜYÜK OLANLARI VE KOMÝSYON ORANLARI BOÞ OLMAYANLARI LÝSTELESÝN.
SELECT * FROM EMPLOYEES WHERE SALARY > 1000 AND COMISSION IS NOT NULL;

-- DEPARTMENT_ID KOLONUNA GÖRE SIRALASIN.
SELECT * FROM EMPLOYEES ORDER BY DEPARTMENT_ID;

-- DEPARTMENT_ID KOLONUNA GÖRE AZALAN, MAAÞ KOLONUNA GÖRE ARTAN BÝÇÝMDE SIRALASIN.
SELECT * FROM EMPLOYEES ORDER BY DEPARTMENT_ID DESC, SALARY ASC;

